package com.dicoding.javafundamental.operator;

public class OperatorAritmatika {
    public static void main(String[]args){
        System.out.println("Operasi Penjumlahan");
        int hasiltambah = 5 + 4;
        System.out.println("Hasil penjumlahan dari 5 + 4 adalah =" + hasiltambah);
        System.out.println();

        System.out.println("Operasi Pengurangan");
        int hasilkurang = 10 - 3;
        System.out.println("Hasil pengurangan dari 10 - 3 = " + hasilkurang);
        System.out.println();

        System.out.println("Operasi Perkalian");
        int hasilkali = 4*5;
        System.out.println("Hasil perkalian dari 4 x 5 = " +hasilkali);
        System.out.println();

        System.out.println("Operasi Pembagian");
        int hasilbagi = 12/4;
        System.out.println("Hasil pembagian dari 12 / 4 = " + hasilbagi);
        System.out.println();

        System.out.println("Hasil sisa hasil pembagian");
        int hasilsisa1 = 8%2;
        System.out.println("Hasil dari 8%2 = " + hasilsisa1);
        System.out.println();
        int hasilsisa2 = 9%2;
        System.out.println("Hasil dari 9%2 = " + hasilsisa2);
        System.out.println();

    }

}
